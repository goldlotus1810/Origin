/// Extract word hashes from text (for silk edge creation).
pub fn text_to_word_hashes(text: &str) -> Vec<u64> {
    text.split_whitespace()
        .filter(|w| w.chars().count() > 2) // skip short words
        .map(|w| fnv1a_str(&w.to_lowercase()))
        .collect()
}

