    #[test]
    fn knowtree_serialization_roundtrip() {
        let kt = KnowTree::bootstrap_from_ucd();
        let bytes = kt.to_bytes();
        let restored = KnowTree::from_bytes(&bytes).unwrap();

        assert_eq!(restored.group_count(), kt.group_count());
        assert_eq!(restored.block_count(), kt.block_count());
        assert_eq!(restored.count(), kt.count());
        assert_eq!(restored.l3.len(), kt.l3.len());

        // Check a known codepoint
        assert_eq!(restored.get(0x1F525), kt.get(0x1F525));

        // Check topology preserved
        for gi in 0..kt.group_count() {
            assert_eq!(restored.group_p_weight(gi), kt.group_p_weight(gi));
            assert_eq!(restored.group_block_range(gi), kt.group_block_range(gi));
        }
        for bi in 0..kt.block_count() {
            assert_eq!(restored.block_p_weight(bi), kt.block_p_weight(bi));
            assert_eq!(restored.block_char_range(bi), kt.block_char_range(bi));
        }
    }

