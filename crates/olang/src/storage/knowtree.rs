//! # knowtree — Hierarchical KnowTree + Legacy wrappers
//!
//! ## KnowTree (hierarchical, T14)
//!
//! ```text
//! L0 (groups)  → 4 entries   (SDF, MATH, EMOTICON, MUSICAL)
//! L1 (blocks)  → ~59 entries (Unicode blocks)
//! L3 (leaves)  → ~8,846 entries (same order as UCD_TABLE)
//! Topology: L0→L1→L3 via Range indices
//! ~18 KB RAM total
//! ```
//!
//! ## Legacy types (sẽ bị xóa ở T10)
//!
//! - `KnowTreeLegacy` — old TieredStore wrapper
//! - `SlimKnowTreeLegacy` — old SlimNode/SlimPage wrapper

