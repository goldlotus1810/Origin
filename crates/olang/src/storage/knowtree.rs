use core::ops::Range;

/// KnowTree — hierarchical tree mapping L0 (groups) → L1 (blocks) → L3 (leaves).
///
/// ```text
/// L0: group P_weights      (4 entries, ~8B)
/// L1: block P_weights      (~59 entries, ~118B)
/// L3: leaf P_weights       (~8,846 entries, ~17.7KB)
/// Topology: l0_to_l1 + l1_to_l3 ranges (~500B)
/// Total: ~18 KB
/// ```
///
/// Leaf P_weights are in the same order as UCD_TABLE entries,
/// grouped by block (which are grouped by group).
pub struct KnowTree {
    /// L0: group P_weights (4-5 entries)
    l0: Vec<u16>,
    /// L1: block P_weights (~59 entries)
    l1: Vec<u16>,
    /// L3: leaf P_weights (same order as UCD_TABLE)
    l3: Vec<u16>,
    /// Topology: which L1 blocks belong to which L0 group
    l0_to_l1: Vec<Range<u16>>,
    /// Topology: which L3 chars belong to which L1 block
    l1_to_l3: Vec<Range<u16>>,
    /// Number of filled leaf entries
    count: u32,
}

impl KnowTree {
    /// Create empty KnowTree.
    pub fn new() -> Self {
        Self {
            l0: Vec::new(),
            l1: Vec::new(),
            l3: Vec::new(),
            l0_to_l1: Vec::new(),
            l1_to_l3: Vec::new(),
            count: 0,
        }
    }

    /// Bootstrap from UCD crate's generated data.
    ///
    /// Builds the full hierarchy from the static tables generated by ucd/build.rs.
    /// L0 entries are SEALED (never overwritten).
    pub fn bootstrap_from_ucd() -> Self {
        let groups = ucd::groups();
        let blocks = ucd::blocks();
        let table = ucd::table();

        let mut l0 = Vec::with_capacity(groups.len());
        let mut l0_to_l1 = Vec::with_capacity(groups.len());

        // Build L0 (groups) and topology
        let mut l1_cursor: u16 = 0;
        for &(_, pw, block_indices) in groups {
            l0.push(pw);
            let start = l1_cursor;
            l1_cursor += block_indices.len() as u16;
            l0_to_l1.push(start..l1_cursor);
        }

        // Build L1 (blocks) and L1→L3 topology
        let mut l1 = Vec::with_capacity(blocks.len());
        let mut l1_to_l3 = Vec::with_capacity(blocks.len());

        for &(_, pw, chars_start, chars_count) in blocks {
            l1.push(pw);
            l1_to_l3.push(chars_start..(chars_start + chars_count));
        }

        // Build L3 (leaves) — P_weights in same order as UCD_TABLE
        let l3: Vec<u16> = table.iter().map(|e| e.p_weight).collect();
        let count = l3.iter().filter(|&&pw| pw != 0).count() as u32;

        Self {
            l0,
            l1,
            l3,
            l0_to_l1,
            l1_to_l3,
            count,
        }
    }

    /// Lookup P_weight for a codepoint (delegates to ucd::p_weight_of).
    #[inline]
    pub fn get(&self, cp: u32) -> u16 {
        ucd::p_weight_of(cp)
    }

    /// Lookup Molecule for a codepoint.
    #[inline]
    pub fn get_mol(&self, cp: u32) -> Option<Molecule> {
        let pw = self.get(cp);
        if pw != 0 {
            Some(Molecule::from_u16(pw))
        } else {
            None
        }
    }

    /// Check if codepoint has a known P_weight.
    #[inline]
    pub fn contains(&self, cp: u32) -> bool {
        self.get(cp) != 0
    }

    /// Number of filled leaf entries.
    #[inline]
    pub fn count(&self) -> u32 {
        self.count
    }

    /// Number of L0 groups.
    #[inline]
    pub fn group_count(&self) -> usize {
        self.l0.len()
    }

    /// Number of L1 blocks.
    #[inline]
    pub fn block_count(&self) -> usize {
        self.l1.len()
    }

    /// P_weight of a group (L0).
    #[inline]
    pub fn group_p_weight(&self, group_idx: usize) -> u16 {
        self.l0.get(group_idx).copied().unwrap_or(0)
    }

    /// P_weight of a block (L1).
    #[inline]
    pub fn block_p_weight(&self, block_idx: usize) -> u16 {
        self.l1.get(block_idx).copied().unwrap_or(0)
    }

    /// Block indices belonging to a group.
    pub fn group_block_range(&self, group_idx: usize) -> Range<u16> {
        self.l0_to_l1
            .get(group_idx)
            .cloned()
            .unwrap_or(0..0)
    }

    /// Char indices belonging to a block.
    pub fn block_char_range(&self, block_idx: usize) -> Range<u16> {
        self.l1_to_l3
            .get(block_idx)
            .cloned()
            .unwrap_or(0..0)
    }

    /// Sample P_weights by dimension value from L3 (leaf nodes).
    ///
    /// VM.7: Lấy mẫu trực tiếp từ KnowTree thay vì file json ngoài.
    /// Dùng cho formula_adaptive — tính ValenceState/ArousalState từ data thật.
    ///
    /// dim: 0=S, 1=R, 2=V, 3=A, 4=T
    /// value: giá trị dimension cần filter (e.g., V=6)
    /// max_samples: số mẫu tối đa (Fib-sized)
    pub fn sample_by_dim(&self, dim: u8, value: u8, max_samples: usize) -> Vec<u16> {
        let matching: Vec<u16> = self.l3.iter()
            .filter(|&&pw| {
                if pw == 0 { return false; }
                let extracted = match dim {
                    0 => ((pw >> 12) & 0x0F) as u8, // S
                    1 => ((pw >> 8) & 0x0F) as u8,  // R
                    2 => ((pw >> 5) & 0x07) as u8,   // V
                    3 => ((pw >> 2) & 0x07) as u8,   // A
                    4 => (pw & 0x03) as u8,           // T
                    _ => return false,
                };
                extracted == value
            })
            .copied()
            .collect();

        let k = max_samples.min(matching.len());
        matching[..k].to_vec()
    }

    /// Get all L3 P_weights (for formula_adaptive fallback).
    pub fn l3_weights(&self) -> &[u16] {
        &self.l3
    }

    /// RAM usage in bytes.
    #[inline]
    pub fn ram_usage(&self) -> usize {
        self.l0.len() * 2
            + self.l1.len() * 2
            + self.l3.len() * 2
            + self.l0_to_l1.len() * 4  // Range<u16> = 2 × u16
            + self.l1_to_l3.len() * 4
            + 4 // count field
    }

    /// Summary string.
    pub fn summary(&self) -> String {
        format!(
            "KnowTree: L0={} groups, L1={} blocks, L3={} chars, ~{}KB",
            self.l0.len(),
            self.l1.len(),
            self.l3.len(),
            self.ram_usage() / 1024,
        )
    }

    /// Serialize to bytes.
    ///
    /// Format:
    /// ```text
    /// [magic:2B "KT"][version:1B][l0_count:1B][l1_count:2B][l3_count:2B]
    /// [l0 p_weights: l0_count × 2B]
    /// [l1 p_weights: l1_count × 2B]
    /// [l3 p_weights: l3_count × 2B]
    /// [l0_to_l1 topology: l0_count × 4B (start:2B, end:2B)]
    /// [l1_to_l3 topology: l1_count × 4B (start:2B, end:2B)]
    /// ```
    pub fn to_bytes(&self) -> Vec<u8> {
        let header_size = 2 + 1 + 1 + 2 + 2; // magic + version + l0_count + l1_count + l3_count
        let data_size = self.l0.len() * 2
            + self.l1.len() * 2
            + self.l3.len() * 2
            + self.l0_to_l1.len() * 4
            + self.l1_to_l3.len() * 4;
        let mut buf = Vec::with_capacity(header_size + data_size);

        // Header
        buf.extend_from_slice(b"KT");           // magic
        buf.push(0x01);                          // version
        buf.push(self.l0.len() as u8);           // l0_count
        buf.extend_from_slice(&(self.l1.len() as u16).to_be_bytes()); // l1_count
        buf.extend_from_slice(&(self.l3.len() as u16).to_be_bytes()); // l3_count

        // L0 P_weights
        for &pw in &self.l0 {
            buf.extend_from_slice(&pw.to_be_bytes());
        }

        // L1 P_weights
        for &pw in &self.l1 {
            buf.extend_from_slice(&pw.to_be_bytes());
        }

        // L3 P_weights
        for &pw in &self.l3 {
            buf.extend_from_slice(&pw.to_be_bytes());
        }

        // L0→L1 topology
        for r in &self.l0_to_l1 {
            buf.extend_from_slice(&r.start.to_be_bytes());
            buf.extend_from_slice(&r.end.to_be_bytes());
        }

        // L1→L3 topology
        for r in &self.l1_to_l3 {
            buf.extend_from_slice(&r.start.to_be_bytes());
            buf.extend_from_slice(&r.end.to_be_bytes());
        }

        buf
    }

    /// Restore from bytes.
    pub fn from_bytes(b: &[u8]) -> Option<Self> {
        if b.len() < 8 {
            return None;
        }
        // Check magic
        if b[0] != b'K' || b[1] != b'T' {
            return None;
        }
        let _version = b[2];
        let l0_count = b[3] as usize;
        let l1_count = u16::from_be_bytes([b[4], b[5]]) as usize;
        let l3_count = u16::from_be_bytes([b[6], b[7]]) as usize;

        let expected_len = 8
            + l0_count * 2
            + l1_count * 2
            + l3_count * 2
            + l0_count * 4
            + l1_count * 4;
        if b.len() < expected_len {
            return None;
        }

        let mut offset = 8;

        // Read L0 P_weights
        let mut l0 = Vec::with_capacity(l0_count);
        for _ in 0..l0_count {
            l0.push(u16::from_be_bytes([b[offset], b[offset + 1]]));
            offset += 2;
        }

        // Read L1 P_weights
        let mut l1 = Vec::with_capacity(l1_count);
        for _ in 0..l1_count {
            l1.push(u16::from_be_bytes([b[offset], b[offset + 1]]));
            offset += 2;
        }

        // Read L3 P_weights
        let mut l3 = Vec::with_capacity(l3_count);
        for _ in 0..l3_count {
            l3.push(u16::from_be_bytes([b[offset], b[offset + 1]]));
            offset += 2;
        }

        // Read L0→L1 topology
        let mut l0_to_l1 = Vec::with_capacity(l0_count);
        for _ in 0..l0_count {
            let start = u16::from_be_bytes([b[offset], b[offset + 1]]);
            let end = u16::from_be_bytes([b[offset + 2], b[offset + 3]]);
            l0_to_l1.push(start..end);
            offset += 4;
        }

        // Read L1→L3 topology
        let mut l1_to_l3 = Vec::with_capacity(l1_count);
        for _ in 0..l1_count {
            let start = u16::from_be_bytes([b[offset], b[offset + 1]]);
            let end = u16::from_be_bytes([b[offset + 2], b[offset + 3]]);
            l1_to_l3.push(start..end);
            offset += 4;
        }

        let count = l3.iter().filter(|&&pw| pw != 0).count() as u32;

        Some(Self {
            l0,
            l1,
            l3,
            l0_to_l1,
            l1_to_l3,
            count,
        })
    }
}

impl Default for KnowTree {
    fn default() -> Self {
        Self::new()
    }
}

